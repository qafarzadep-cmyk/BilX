-- Notify the admin (used when a student submits a teacher application). Resolves
-- the admin's user_id from auth.users so the caller does not need to know it.
create or replace function public.notify_admin(
  p_title text,
  p_body text default null,
  p_link text default null
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  admin_id uuid;
begin
  if auth.uid() is null then
    raise exception 'Must be authenticated';
  end if;

  select id into admin_id
  from auth.users
  where lower(email) = 'qafarzadep@gmail.com'
  limit 1;

  if admin_id is null then
    return;
  end if;

  insert into public.notifications (user_id, title, body, link)
  values (admin_id, p_title, p_body, p_link);
end;
$$;
grant execute on function public.notify_admin(text, text, text) to authenticated;

-- Admin-only user directory with real emails + signup dates from auth.users.
-- Lets the admin panel show emails without exposing them through the public
-- profiles table. Also reports whether each user is currently banned.
drop function if exists public.admin_list_users();
create or replace function public.admin_list_users()
returns table (
  user_id uuid,
  email text,
  full_name text,
  role text,
  created_at timestamptz,
  banned boolean,
  last_active timestamptz,
  device_info text
)
language sql
security definer
set search_path = public
stable
as $$
  select u.id,
         u.email::text,
         coalesce(p.full_name, u.raw_user_meta_data ->> 'full_name', u.email)::text,
         coalesce(p.role, 'student')::text,
         u.created_at,
         (u.banned_until is not null and u.banned_until > now()) as banned,
         s.last_active,
         s.device_info
  from auth.users u
  left join public.profiles p on p.user_id = u.id
  left join public.user_sessions s on s.user_id = u.id
  where public.is_admin()
  order by u.created_at desc;
$$;
grant execute on function public.admin_list_users() to authenticated;

-- Admin-only: ban / unban a user (GoTrue blocks login while banned_until is in
-- the future). The admin account cannot ban itself.
create or replace function public.admin_set_user_banned(p_user_id uuid, p_banned boolean)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  target_email text;
begin
  if not public.is_admin() then
    raise exception 'Only admin can ban users';
  end if;
  if p_user_id is null then
    return;
  end if;

  select email into target_email from auth.users where id = p_user_id;
  if lower(coalesce(target_email, '')) = 'qafarzadep@gmail.com' then
    raise exception 'Cannot ban the admin account';
  end if;

  update auth.users
  set banned_until = case when p_banned then 'infinity'::timestamptz else null end
  where id = p_user_id;
end;
$$;
grant execute on function public.admin_set_user_banned(uuid, boolean) to authenticated;

-- Admin-only: permanently delete a user. Most related rows cascade via FK; the
-- email-keyed enrollments do not, so they are removed explicitly. The admin
-- account cannot delete itself.
create or replace function public.admin_delete_user(p_user_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  target_email text;
begin
  if not public.is_admin() then
    raise exception 'Only admin can delete users';
  end if;
  if p_user_id is null then
    return;
  end if;

  select email into target_email from auth.users where id = p_user_id;
  if lower(coalesce(target_email, '')) = 'qafarzadep@gmail.com' then
    raise exception 'Cannot delete the admin account';
  end if;

  delete from public.enrollments
  where user_id = p_user_id::text
     or lower(user_id) = lower(coalesce(target_email, ''));

  delete from auth.users where id = p_user_id;
end;
$$;
grant execute on function public.admin_delete_user(uuid) to authenticated;

-- New auth users always get a student profile (role from metadata is ignored).
create or replace function public.create_profile_for_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (user_id, full_name, role)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'full_name', new.email),
    'student'
  )
  on conflict (user_id) do update
    set full_name = coalesce(excluded.full_name, public.profiles.full_name);
  return new;
end;
$$;

drop trigger if exists create_profile_after_signup on auth.users;
create trigger create_profile_after_signup
  after insert on auth.users
  for each row execute function public.create_profile_for_new_user();

