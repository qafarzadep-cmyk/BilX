-- ============================================================================
-- Bil-X consolidated reconcile script.
--
-- Idempotent. Brings an existing (possibly older) database fully up to the
-- canonical schema in a single run: tables, missing columns, column types,
-- constraints, grants, RLS policies, functions and triggers. Safe to run
-- repeatedly. Supersedes running bilx_schema.sql + teacher_applications.sql +
-- phase2_schema.sql + the various fix_* / phase1_* patches individually.
--
-- The only thing it cannot auto-resolve is non-numeric data in a column being
-- converted to bigint (enrollments.course_id). If that errors, clean the data
-- and re-run.
-- ============================================================================

