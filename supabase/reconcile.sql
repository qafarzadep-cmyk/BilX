-- ---------------------------------------------------------------------------
-- 8b. Public, URL-free lesson list for published courses. Lets prospective
--     buyers (and logged-out visitors) see the full curriculum — titles,
--     durations, which lessons are free — without exposing paid video URLs.
--     The view is owned by the migration role, so it bypasses videos RLS and
--     returns every lesson of a published course; the sensitive video_url
--     column is simply not selected.
-- ---------------------------------------------------------------------------
drop view if exists public.lesson_previews;
create view public.lesson_previews as
  select v.id, v.course_id, v.title, v.duration, v.order_index, v.is_free
  from public.videos v
  join public."Courses" c on c.id = v.course_id
  where c.is_published = true;
grant select on public.lesson_previews to anon, authenticated;

-- ---------------------------------------------------------------------------
-- 9. Backfill: course/instructor display names from approved applications.
-- ---------------------------------------------------------------------------
update public.profiles p
set full_name = trim(a.name || ' ' || a.surname)
from public.teacher_applications a
where p.user_id = a.user_id
  and a.status = 'approved'
  and nullif(trim(a.name || ' ' || a.surname), '') is not null
  and (p.full_name is null or trim(p.full_name) = '');

update public."Courses" c
set instructor_name = p.full_name
from public.profiles p
where c.instructor_id = p.user_id
  and (c.instructor_name is null or trim(c.instructor_name) = '')
  and nullif(trim(p.full_name), '') is not null;

-- Backfill order_index on older lessons that have none, so the curriculum sorts
-- correctly and the first-lesson preview resolves. Uses insertion order (id).
update public.videos v
set order_index = ranked.rn
from (
  select id, row_number() over (partition by course_id order by id) as rn
  from public.videos
  where order_index is null or order_index = 0
) ranked
where v.id = ranked.id;

-- ---------------------------------------------------------------------------
-- 10. Normalize enrollments to email-keyed (issue #11). The admin grants access
--     by email, so email is the canonical key. Convert any legacy UUID-keyed
--     rows to the owner's lowercased email. First drop a UUID row when an email
--     row already exists for the same course (avoids a unique-constraint clash).
-- ---------------------------------------------------------------------------
delete from public.enrollments e
using auth.users u
where e.user_id = u.id::text
  and exists (
    select 1 from public.enrollments e2
    where e2.course_id = e.course_id
      and lower(e2.user_id) = lower(u.email)
  );

update public.enrollments e
set user_id = lower(u.email)
from auth.users u
where e.user_id = u.id::text
  and u.email is not null;

-- ---------------------------------------------------------------------------
-- 11. Single active session per user (concurrent-session limiting, Option A).
--     Each login writes a fresh session_token here, overwriting the previous
--     one ("invalidate old session on new login"). The app subscribes to this
--     row via Realtime and logs out a device whose stored token no longer
--     matches. One row per user (user_id is the primary key).
-- ---------------------------------------------------------------------------
create table if not exists public.user_sessions (
  user_id uuid primary key references auth.users(id) on delete cascade,
  session_token text not null,
  last_active timestamptz not null default now(),
  device_info text,
  updated_at timestamptz not null default now()
);

grant select, insert, update on public.user_sessions to authenticated;

alter table public.user_sessions enable row level security;

drop policy if exists "user_sessions_own" on public.user_sessions;
create policy "user_sessions_own"
  on public.user_sessions for all
  using (user_id = auth.uid())
  with check (user_id = auth.uid());

-- Stream changes to the owner so the previous device is kicked instantly.
do $$
begin
  alter publication supabase_realtime add table public.user_sessions;
exception
  when duplicate_object then null;
  when others then null;
end $$;

