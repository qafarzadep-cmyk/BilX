-- Single source of truth for "is the current user the admin". All policies and
-- admin RPCs call this, so changing the admin means editing one place here (plus
-- ADMIN_EMAIL in src/profileApi.js on the app side).
create or replace function public.is_admin()
returns boolean
language sql
stable
as $$
  select lower(coalesce(auth.jwt() ->> 'email', '')) = 'qafarzadep@gmail.com';
$$;
grant execute on function public.is_admin() to authenticated, anon;

-- Instructor-role check that bypasses RLS (avoids Courses <-> profiles recursion).
create or replace function public.is_approved_instructor()
returns boolean
language sql
security definer
set search_path = public
stable
as $$
  select exists (
    select 1 from public.profiles
    where user_id = auth.uid() and role = 'instructor'
  );
$$;
grant execute on function public.is_approved_instructor() to authenticated, anon;

-- Active-enrollment check that bypasses RLS/grants. Used inside the videos read
-- policy so a logged-out (anon) viewer — who has no grant on enrollments — can
-- still be evaluated against the policy without a "permission denied" error.
create or replace function public.has_course_access(p_course_id bigint)
returns boolean
language sql
security definer
set search_path = public
stable
as $$
  select exists (
    select 1 from public.enrollments e
    where e.course_id::text = p_course_id::text
      and coalesce(e.status, 'active') = 'active'
      and (
        e.user_id::text = auth.uid()::text
        or lower(e.user_id::text) = lower(coalesce(auth.jwt() ->> 'email', ''))
      )
  );
$$;
grant execute on function public.has_course_access(bigint) to authenticated, anon;

-- Admin-only review of teacher applications (atomic status + role update).
create or replace function public.admin_review_teacher_application(application_id bigint, decision text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  target_user uuid;
  target_full_name text;
begin
  if not public.is_admin() then
    raise exception 'Only admin can review teacher applications';
  end if;

  if decision not in ('approved', 'rejected') then
    raise exception 'Decision must be approved or rejected';
  end if;

  select user_id, trim(name || ' ' || surname) into target_user, target_full_name
  from public.teacher_applications
  where id = application_id and status = 'pending';

  if target_user is null then
    raise exception 'Pending application not found';
  end if;

  update public.teacher_applications
  set status = decision, reviewed_at = now()
  where id = application_id;

  if decision = 'approved' then
    insert into public.profiles (user_id, full_name, role)
    values (target_user, target_full_name, 'instructor')
    on conflict (user_id) do update
      set role = 'instructor', full_name = excluded.full_name;
  end if;
end;
$$;
grant execute on function public.admin_review_teacher_application(bigint, text) to authenticated;

