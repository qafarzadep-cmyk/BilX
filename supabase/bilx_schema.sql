create policy "videos_read_for_owner_or_enrolled"
  on public.videos for select
  using (
    exists (select 1 from public."Courses" c where c.id = videos.course_id and c.instructor_id = auth.uid())
    or exists (
      select 1 from public.enrollments e
      where e.course_id::text = videos.course_id::text
        and coalesce(e.status, 'active') = 'active'
        and (e.user_id::text = auth.uid()::text or lower(e.user_id::text) = lower(coalesce(auth.jwt() ->> 'email', '')))
    )
    or lower(coalesce(auth.jwt() ->> 'email', '')) = 'qafarzadep@gmail.com'
  );

create policy "videos_insert_for_course_owner"
  on public.videos for insert
  with check (exists (select 1 from public."Courses" c where c.id = videos.course_id and c.instructor_id = auth.uid()));

create policy "videos_update_for_course_owner"
  on public.videos for update
  using (exists (select 1 from public."Courses" c where c.id = videos.course_id and c.instructor_id = auth.uid()))
  with check (exists (select 1 from public."Courses" c where c.id = videos.course_id and c.instructor_id = auth.uid()));

create policy "videos_delete_for_course_owner"
  on public.videos for delete
  using (exists (select 1 from public."Courses" c where c.id = videos.course_id and c.instructor_id = auth.uid()));

